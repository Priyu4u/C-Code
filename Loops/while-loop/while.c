#include <stdio.h>
int main( ){
    // Print counting numbers from 1 to 100 using while loop
    int i=1;  // initialisation before loop
    while (i<=100){    // condition inside the loop
        printf("%d ",i);
        i++;   // increment inside the loop
    }

    // while loop is used when we do not have enough information
    return 0;
} 